// Intersection Observer for scroll animations
const observerOptions = {
    threshold: 0.2,
    rootMargin: '0px 0px -100px 0px'
};

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.style.opacity = '1';
            entry.target.style.transform = 'translateY(0)';
            
            // Animate progress bars
            if (entry.target.classList.contains('timeline-content')) {
                const progressFill = entry.target.querySelector('.progress-fill');
                if (progressFill) {
                    const progress = progressFill.getAttribute('data-progress');
                    setTimeout(() => {
                        progressFill.style.width = progress + '%';
                    }, 200);
                }
            }
            
            // Animate growth bars
            if (entry.target.classList.contains('growth-chart')) {
                const growthBars = entry.target.querySelectorAll('.growth-bar');
                growthBars.forEach((bar, index) => {
                    const height = bar.getAttribute('data-height');
                    const fill = bar.querySelector('.growth-fill');
                    setTimeout(() => {
                        fill.style.height = height + '%';
                    }, 200 + (index * 100));
                });
            }
            
            observer.unobserve(entry.target);
        }
    });
}, observerOptions);

